# Constants
CELL_SIZE = 20  # Reduced size of each cell for a smaller screen
SCREEN_WIDTH = len(boards[0]) * CELL_SIZE  # Screen width based on map size
SCREEN_HEIGHT = len(boards) * CELL_SIZE  # Screen height based on map size

# Colors
BLACK = (0, 0, 0)  # Empty space
WHITE = (255, 255, 255)  # Walls or ghost house area
YELLOW = (255, 255, 0)  # Dot color
BLUE = (0, 0, 255)  # Walls and all other areas that need to be blue
RED = (255, 0, 0)  # Gate color (optional)


# Define element drawing functions
def draw_element(screen, element, x, y):
    if element == 0:
        pygame.draw.rect(screen, BLACK, (x, y, CELL_SIZE, CELL_SIZE))  # Empty rectangle
    elif element == 1:
        pygame.draw.circle(screen, YELLOW, (x + CELL_SIZE // 2, y + CELL_SIZE // 2), CELL_SIZE // 4)  # Dot
    elif element == 2:
        pygame.draw.circle(screen, WHITE, (x + CELL_SIZE // 2, y + CELL_SIZE // 2), CELL_SIZE // 2)  # Big dot
    elif element == 3:
        pygame.draw.rect(screen, BLUE, (x + CELL_SIZE // 2 - 5, y, 10, CELL_SIZE))  # Vertical line (now blue)
    elif element == 4:
        pygame.draw.rect(screen, BLUE, (x, y + CELL_SIZE // 2 - 5, CELL_SIZE, 10))  # Horizontal line (now blue)
    elif element == 5:  # Top right corner
        pygame.draw.polygon(screen, BLUE, [(x + CELL_SIZE, y), (x + CELL_SIZE, y + CELL_SIZE), (x, y + CELL_SIZE)])
    elif element == 6:  # Top left corner
        pygame.draw.polygon(screen, BLUE, [(x, y), (x + CELL_SIZE, y), (x, y + CELL_SIZE)])
    elif element == 7:  # Bottom left corner
        pygame.draw.polygon(screen, BLUE, [(x, y), (x + CELL_SIZE, y), (x + CELL_SIZE, y + CELL_SIZE)])
    elif element == 8:  # Bottom right corner
        pygame.draw.polygon(screen, BLUE, [(x, y + CELL_SIZE), (x + CELL_SIZE, y + CELL_SIZE), (x + CELL_SIZE, y)])
    elif element == 9:  # Gate
        pygame.draw.rect(screen, RED, (x + CELL_SIZE // 4, y + CELL_SIZE // 4, CELL_SIZE // 2, CELL_SIZE // 2))  # Gate


# Draw the entire board
def draw_board(screen, board):
    for row in range(len(board)):
        for col in range(len(board[0])):
            draw_element(screen, board[row][col], col * CELL_SIZE, row * CELL_SIZE)

# Main game loop
def main():
    screen = pygame.display.set_mode((SCREEN_WIDTH, SCREEN_HEIGHT))
    pygame.display.set_caption("Pac-Man Map")
    clock = pygame.time.Clock()

    running = True
    while running:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False

        screen.fill(BLACK)  # Background color
        draw_board(screen, boards)  # Draw the Pac-Man board
        pygame.display.flip()
        clock.tick(60)  # FPS

    pygame.quit()

if __name__ == "__main__":
    main()